"""
Регулярные выражения и ключевые слова для поиска персональных данных (ПДн).

Все паттерны соответствуют категориям ПДн по 152-ФЗ «О персональных данных»:

Категории:
    - **ФИО** — фамилия, имя, отчество
    - **Контактные данные** — телефоны, e-mail
    - **Дата и место рождения**
    - **Адрес** — проживания / регистрации
    - **Паспорт РФ** — серия и номер
    - **СНИЛС** — страховой номер
    - **ИНН** — физических и юридических лиц
    - **Водительское удостоверение**
    - **MRZ** — машиносчитываемая зона документов
    - **Банковские карты** — номер, CVV, срок действия
    - **Банковские счета** — номера счетов и БИК
    - **Биометрия** — упоминания биометрических характеристик
    - **Специальные категории** — здоровье, религия, политика, раса

Соглашение об именовании:
    Каждый паттерн — скомпилированный объект ``re.Pattern``.
    Группы именованные (``(?P<n>...)``), если нужна валидация.

Флаги:
    По умолчанию ``re.IGNORECASE | re.UNICODE``.
    Многострочные паттерны компилируются с ``re.VERBOSE``.
"""

from __future__ import annotations

import re
from typing import NamedTuple

# ---------------------------------------------------------------------------
# Вспомогательные константы
# ---------------------------------------------------------------------------

_F = re.IGNORECASE | re.UNICODE

# Граница слова, совместимая с кириллицей (lookahead/lookbehind)
_WB_L = r"(?<![А-Яа-яЁёA-Za-z0-9_])"
_WB_R = r"(?![А-Яа-яЁёA-Za-z0-9_])"


def _wb(pattern: str) -> str:
    """Оборачивает паттерн в кириллица-совместимые границы слова."""
    return _WB_L + pattern + _WB_R


# ---------------------------------------------------------------------------
# ФИО
# ИСПРАВЛЕНИЕ: добавлен lookbehind на ключевые слова, отчество стало
# обязательным ИЛИ требуется контекстное ключевое слово.
# Это резко снижает срабатывание на названия организаций/городов.
# ---------------------------------------------------------------------------

# Контекстное ФИО: ключевое слово + Фамилия Имя [Отчество]
# Ключевое слово должно стоять непосредственно перед именем.
# После ключевого слова — сразу заглавная буква (начало фамилии).
_FULL_NAME_RU_CONTEXT = re.compile(
    r"""
    (?:
        ФИО|Ф\.И\.О\.|фамилия|заявитель(?:я|ю|ем|е)?|сотрудник(?:а|у|ом|е)?|
        работник(?:а|у|ом|е)?|клиент(?:а|у|ом|е)?|
        владелец|держатель|собственник|представитель|
        застрахованный|получатель|плательщик|абонент
    )
    [ \t]*:?[ \t]*\n?[ \t]*              # разделитель: пробелы, двоеточие, перенос
    (?=[А-ЯЁ])                          # следующий символ — заглавная (начало фамилии)
    (?P<last>   [А-ЯЁ][а-яё]{1,20}-?[а-яё]*)   # Фамилия
    [ \t]+
    (?P<first>  [А-ЯЁ][а-яё]{1,15})            # Имя
    (?:
        [ \t]+
        (?P<middle> [А-ЯЁ][а-яё]+(?:вич|вна|ович|овна|евич|евна))   # Отчество
    )?
    """,
    re.VERBOSE | re.IGNORECASE | re.UNICODE,
)

# Безконтекстное ФИО — только если отчество ПРИСУТСТВУЕТ (тройка слов)
# Отчество — достаточно надёжный признак ФИО в русском тексте
_FULL_NAME_RU_STRICT = re.compile(
    r"""
    (?<![А-ЯЁа-яё])                            # не середина слова
    (?P<last>   [А-ЯЁ][а-яё]{1,20}-?[а-яё]*)   # Фамилия (мин 2 символа)
    \s+
    (?P<first>  [А-ЯЁ][а-яё]{1,15})            # Имя
    \s+
    (?P<middle> [А-ЯЁ][а-яё]+(?:вич|вна|ович|овна|евич|евна))   # Отчество обязательно
    (?![А-ЯЁа-яё])
    """,
    re.VERBOSE | re.UNICODE,
)

# Инициалы рядом с фамилией: «Иванов И.И.» / «И.И. Иванов»
_INITIALS_NAME_RU = re.compile(
    r"""
    (?:
        [А-ЯЁ][а-яё]{1,20}-?[а-яё]*\s+[А-ЯЁ]\.[А-ЯЁ]\.   # Фамилия И.О.
        |
        [А-ЯЁ]\.[А-ЯЁ]\.\s+[А-ЯЁ][а-яё]{1,20}-?[а-яё]*   # И.О. Фамилия
    )
    """,
    re.VERBOSE | re.UNICODE,
)


# ---------------------------------------------------------------------------
# Телефон
# ---------------------------------------------------------------------------

_PHONE_RU = re.compile(
    r"""
    (?<!\d)
    (?:
        (?:\+7|8)                         # код страны
        [\s\-\(]*
        (?:\d{3})                         # код региона (3 цифры)
        [\s\-\)]*
        \d{3}                             # первые 3 цифры номера
        [\s\-]?
        \d{2}                             # следующие 2
        [\s\-]?
        \d{2}                             # последние 2
    )
    (?!\d)
    """,
    re.VERBOSE | re.UNICODE,
)

# ---------------------------------------------------------------------------
# Email
# ---------------------------------------------------------------------------

_EMAIL = re.compile(
    r"""
    (?<![A-Za-z0-9._%+\-])
    [A-Za-z0-9._%+\-]{1,64}
    @
    [A-Za-z0-9.\-]{1,253}
    \.[A-Za-z]{2,10}
    (?![A-Za-z0-9._%+\-@])
    """,
    re.VERBOSE | re.UNICODE,
)


# ---------------------------------------------------------------------------
# Дата рождения
# ИСПРАВЛЕНИЕ: требуется контекстное ключевое слово «дата рождения» /
# «д.р.» / «дата:» и т.п. — иначе ловятся все даты документов, версий и т.д.
# ---------------------------------------------------------------------------

_DATE_OF_BIRTH = re.compile(
    r"""
    (?:
        дата\s+рождения|д\.?\s*р\.?|
        дата\s*[:]\s*|
        birth(?:day|date)?[\s:]*|
        born[\s:]*|
        DOB[\s:]*
    )
    [\s:]*
    (?:
        (?:0?[1-9]|[12]\d|3[01])   # день
        [.\-/]
        (?:0?[1-9]|1[0-2])         # месяц
        [.\-/]
        (?:19|20)\d{2}             # год (1900-2099)
        |
        (?:19|20)\d{2}             # ISO: год
        -
        (?:0?[1-9]|1[0-2])
        -
        (?:0?[1-9]|[12]\d|3[01])
    )
    """,
    re.VERBOSE | re.IGNORECASE | re.UNICODE,
)

# Словесная дата рождения: «23 января 1985 года» — достаточно уникальна сама по себе
_DATE_VERBAL = re.compile(
    r"""
    (?<!\d)
    (?:0?[1-9]|[12]\d|3[01])\s+
    (?:январ[яь]|феврал[яь]|март[аe]?|апрел[яь]|ма[йя]|июн[яь]|
       июл[яь]|август[аe]?|сентябр[яь]|октябр[яь]|ноябр[яь]|декабр[яь])
    \s+
    (?:19|20)\d{2}
    (?:\s+года?)?
    """,
    re.VERBOSE | re.IGNORECASE | re.UNICODE,
)


# ---------------------------------------------------------------------------
# Адрес
# ---------------------------------------------------------------------------

_ADDRESS_RU = re.compile(
    r"""
    (?<![А-ЯЁа-яёA-Za-z])      # не внутри слова (не «Контракт»)
    (?:
        (?:ул(?:ица)?\.?\s*|пр(?:-?т|оспект)?\.?\s*|пер(?:еулок)?\.?\s*|
           бул(?:ьвар)?\.?\s*|наб(?:ережная)?\.?\s*|пл(?:ощадь)?\.?\s*|
           ш(?:оссе)?\.?\s*|тракт\.?\s*)
        [А-ЯЁа-яёA-Za-z0-9\s\-\.]{2,50}
        [,\s]+
        (?:д(?:ом)?\.?\s*\d+[а-яА-Я]?(?:/\d+)?)?
        (?:[,\s]+кв(?:артира)?\.?\s*\d+)?
    )
    """,
    re.VERBOSE | re.IGNORECASE | re.UNICODE,
)

# ---------------------------------------------------------------------------
# Почтовый индекс
# ИСПРАВЛЕНИЕ: требуется контекстное слово «индекс» или формат «г. Москва, 101000»
# Голый 6-значный номер — слишком много ложных срабатываний (суммы, артикулы, тел.)
# ---------------------------------------------------------------------------

_POSTAL_CODE_RU = re.compile(
    r"""
    (?:
        (?:почтовый\s+)?индекс[\s:]*          # «индекс: 101000»
        |
        (?:^|[,\s])(?:г\.|город|обл\.|область|р-н|район)[\s.]*\S+\s*,\s*  # «г. Москва, 101000»
    )
    (?P<zip>[1-6]\d{5})
    (?!\d)
    """,
    re.VERBOSE | re.IGNORECASE | re.UNICODE,
)


# ---------------------------------------------------------------------------
# Паспорт РФ
# ИСПРАВЛЕНИЕ: безконтекстный паттерн (_PASSPORT_RF) удалён из реестра.
# Оставлен только контекстный — он точнее и не дублирует находки.
# ---------------------------------------------------------------------------

# Контекстный паттерн: ключевое слово + серия/номер — ЕДИНСТВЕННЫЙ паттерн паспорта
_PASSPORT_RF_CONTEXT = re.compile(
    r"""
    (?:паспорт(?:а|е|у|ом)?|passport|пасп\.?|серия\s+и\s+номер|документ\s+№)
    [\s:№#]*
    \d{2}\s?\d{2}[\s\-]?\d{6}
    """,
    re.VERBOSE | re.IGNORECASE | re.UNICODE,
)

# MRZ (Machine Readable Zone)
_MRZ_LINE = re.compile(
    r"""
    [A-Z0-9<]{30,44}        # MRZ-строка TD1/TD3
    """,
    re.VERBOSE | re.UNICODE,
)


# ---------------------------------------------------------------------------
# СНИЛС
# ---------------------------------------------------------------------------

_SNILS = re.compile(
    r"""
    (?<!\d)
    (?P<snils>
        \d{3}[\s\-]\d{3}[\s\-]\d{3}[\s\-]\d{2}   # только с разделителями: 123-456-789 01
        |
        (?:снилс|snils|страховой\s+номер)[\s:№#]*\d{11}   # или с контекстом без разделителей
    )
    (?!\d)
    """,
    re.VERBOSE | re.IGNORECASE | re.UNICODE,
)


# ---------------------------------------------------------------------------
# ИНН
